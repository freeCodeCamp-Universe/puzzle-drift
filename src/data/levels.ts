import type { Level, Position, TileType } from '../types/game';

const TILE_BY_SYMBOL = {
  '.': 'floor',
  '#': 'wall',
  E: 'exit',
  K: 'key',
  D: 'door',
  S: 'switch',
  P: 'pressurePlate',
  B: 'pushBlock',
  O: 'portal',
  X: 'spike',
  I: 'ice',
} as const satisfies Record<string, TileType>;

type LevelInput = Omit<Level, 'grid'> & {
  grid: string[];
};

function position(x: number, y: number): Position {
  return { x, y };
}

function parseGrid(rows: string[]): TileType[][] {
  return rows.map((row) =>
    [...row].map((symbol) => {
      const tile = TILE_BY_SYMBOL[symbol as keyof typeof TILE_BY_SYMBOL];

      if (!tile) {
        throw new Error(`Unknown level tile symbol: ${symbol}`);
      }

      return tile;
    }),
  );
}

function defineLevel(level: LevelInput): Level {
  return {
    ...level,
    grid: parseGrid(level.grid),
  };
}

